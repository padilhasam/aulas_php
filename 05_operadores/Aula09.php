<?php

	//OPERADORES INCREMENTAIS E DECREMENTAIS
	
	$a = 10;
	$b = 20; 
	
		echo "A = ";
		echo $a++; //Exibe como resultado o valor original e depois soma +1, ou seja, $a = 10 e no próximo echo $a = 11;
			echo "<br>";
		echo "A = ";
		echo $a; // Exibe o valor de $a após o incremento, ou seja, $a = 11;
			echo "<br>";
		
		echo "B = ";
		echo ++$b; //Adiciona +1 ao valor original e depois exibe o resultado, ou seja, $b = 21;
		
		/* Primeiro Echo - Mostra primeiro o valor original de A e depois incrementa um;
			Segundo Echo - Mostra valor de A incrementado;
			Terceiro Echo - Incrementa um primeiro e depois mostra o valor; */
					
?>